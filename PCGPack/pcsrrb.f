      SUBROUTINEPCSRRB(N,IBLKN,IBLKS,IBTRM,IRTBM,IBTRMC,NBLCKB,
     *NBLACK,C,IC,IJA,A,RHS,D2I,Y,X)
      IMPLICITDOUBLEPRECISION(A-H,O-Z)
      INTEGERN,IBLKN,IBLKS(*),IBTRM(*),IRTBM(*),IBTRMC(*),
     *NBLCKB,NBLACK,C(*),IC(*),IJA(*)
      DOUBLEPRECISIONA(*),RHS(*),D2I(*),Y(*),X(*)
      LOGICALLOWER,UPPER,FULL,HALF,SYMMAT
      COMMON/PCVERL/LOWER,UPPER,FULL,HALF,SYMMAT
      SAVE/PCVERL/
      CALLPCPRMB(IBLKN-NBLCKB,IBTRM,C(NBLCKB+1),RHS,Y(NBLACK+1))
      CALLPCSC2B(IBLKN,IBTRM,IRTBM,IBTRMC,NBLCKB,C,IC,IJA,A,
     *Y,Y(NBLACK+1))
      CALLPCSD2S(IBLKN,IBLKS,NBLCKB,C,D2I,Y(NBLACK+1))
      CALLPCPRMB(IBLKN,IBTRM,IC,Y,X)
      RETURN
      END
