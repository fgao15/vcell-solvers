      SUBROUTINEPCLUP2(HEAD,LEVP2,NP1,IJU,IUT,U,LEVL,
     *IPT,ROW,SUM,I1,I2)
      IMPLICITDOUBLEPRECISION(A-H,O-Z)
      INTEGERHEAD,LEVP2,NP1,LEVL(*),IPT(*),IJU(*),IUT(*),I1,I2
      DOUBLEPRECISIONU(*),ROW(*),SUM
      INTEGERJ,LL
      DOUBLEPRECISIONPL
      SUM=0.0D0
      IF(HEAD.EQ.0)THEN
      RETURN
      ENDIF
      J=HEAD
      DO1LL=1,I1-1
      IF(J.GE.I1)THEN
      RETURN
      ENDIF
      PL=ROW(J)
      IF(PL.EQ.0.0D0)THEN
      J=IPT(J)
      GOTO1
      ENDIF
      CALLPCLUP3(J,LEVP2,NP1,IJU,IUT,U,LEVL,IPT,ROW,SUM,I1,I2)
      J=IPT(J)
1     CONTINUE
      RETURN
      END
