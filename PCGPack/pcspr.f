      SUBROUTINEPCSPR(N,IJA,A,RHS,X,IPARM,RPARM,NSP,ISP,RSP,
     *METHOD,PRECON,PCSTOP,PCMV)
      IMPLICITDOUBLEPRECISION(A-H,O-Z)
      INTEGERN,IJA(*),IPARM(*),NSP,ISP(*)
      DOUBLEPRECISIONA(*),RHS(*),X(*),RSP(*),RPARM(*)
      EXTERNALMETHOD,PRECON,PCSTOP,PCMV
      INTEGERIERR,ITMAX,ITEST,KMAX,IFL,IPRE,PBLK,LUDONE,IBLK,
     *MNEED,ITS,RSDONE,NOVERF,RED1,RED2,IERR2,IFRMAT
      COMMON/PCCOM1/IERR,ITMAX,ITEST,KMAX,IFL,IPRE,PBLK,
     *LUDONE,IBLK,MNEED,ITS,RSDONE,NOVERF,RED1,RED2,IERR2,
     *IFRMAT
      SAVE/PCCOM1/
      INTEGERIPRE1,IPRE2,RATIO
      COMMON/PCCOM3/IPRE1,IPRE2,RATIO
      SAVE/PCCOM3/
      INTEGERISRT,IJASGN,ISX0,IFORM
      COMMON/PCVERI/ISRT,IJASGN,ISX0,IFORM
      SAVE/PCVERI/
      INTEGERNADR,IRS2,INADR,IJACPY
      CALLPCUNPK(N,IPARM,RPARM)
      IF(IERR.NE.0)THEN
      CALLPCPACK(IPARM,RPARM)
      RETURN
      ENDIF
      CALLPCVERF(N,IJA,X)
      IF(IERR.EQ.20)THEN
      CALLPCDSLV(N,A,RHS,X)
      CALLPCPACK(IPARM,RPARM)
      RETURN
      ELSEIF(IERR.NE.0)THEN
      CALLPCPACK(IPARM,RPARM)
      RETURN
      ENDIF
      CALLPCVERS(N,IJA)
      IRS2=33
      MNEED=(IRS2+RATIO-1)/RATIO
      NADR=(IRS2+RATIO-1)/RATIO+1
      INADR=IRS2+1
      IF(NADR.GT.NSP)THEN
      IERR=10
      MNEED=NSP-NADR
      CALLPCPACK(IPARM,RPARM)
      RETURN
      ENDIF
      IF(IJASGN.EQ.0)THEN
      IJACPY=INADR
      NADR=(INADR+N+RATIO-2)/RATIO+1
      IF(NADR.GT.NSP)THEN
      IERR=10
      MNEED=NSP-NADR
      CALLPCPACK(IPARM,RPARM)
      RETURN
      ENDIF
      MNEED=MNEED+(N+RATIO-1)/RATIO
      CALLPCICPY(N,IJA,ISP(IJACPY))
      ENDIF
      ISP(1)=NSP+1-NADR
      ISP(2)=NADR
      IF(IBLK.EQ.1)THEN
      IF(IJASGN.EQ.0)THEN
      CALLPCIABS(N+1,IJA)
      ENDIF
      CALLPCSRP(N,IJA,A,RHS,X,ISP,RSP,METHOD,PRECON,PCSTOP,PCMV)
      ELSE
      CALLPCSRB(N,IJA,A,RHS,X,ISP,RSP,METHOD,PRECON,PCSTOP,PCMV)
      ENDIF
      IF(IJASGN.EQ.0)THEN
      CALLPCICPY(N,ISP(IJACPY),IJA)
      ENDIF
      IPARM(62)=ISP(19)
      IPARM(63)=ISP(18)
      IPARM(64)=ISP(5)
      IPARM(65)=ISP(6)
      CALLPCPACK(IPARM,RPARM)
      RETURN
      END
